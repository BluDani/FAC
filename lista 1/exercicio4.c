#include<stdio.h>

/*
QUESTÃO 04:
Um banco concederá um crédito especial aos
seus clientes, variável com o saldo médio no
último ano. Faça um algoritmo que calcule o
valor do crédito de acordo com a tabela abaixo.

Saldo Médio 				Percentual
Inferior a R$ 1000,00 		nenhum crédito
De R$ 1000,00 a
R$ 1499,99 					20% do saldo médio
De R$ 1500,00 a
R$ 2499,99 					30% do saldo médio
R$ 2500,00 ou mais 			40% do saldo médio
*/

int main(){
	//saldo medio(sm)
	float sm, credito;
	
	printf("seu saldo medio no ultimo ano:");
	scanf("%f", &sm);
	
	if(sm <1000.00){
		printf("seu saldo e %.2f", sm);
	}
	else{
		if(sm < 1500.00){
			credito = (sm*20)/100;
			printf("seu saldo e %.2f com %.2f de credito", sm, credito);
		}
		else{
			if(sm < 2500.00){
				credito = (sm*30)/100;
				printf("seu saldo e %.2f com %.2f de credito", sm, credito);
			}
			else{
				credito = (sm*40)/100;
				printf("seu saldo e %.2f com %.2f de credito", sm, credito);
			}
		}
	}
}
